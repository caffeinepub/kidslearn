// тФАтФАтФА Utility: speakText тФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФА

export function speakText(text: string, language: string) {
  if (!window.speechSynthesis) return;
  window.speechSynthesis.cancel();
  const utterance = new SpeechSynthesisUtterance(text);
  const langMap: Record<string, string> = {
    english: 'en-US',
    telugu: 'te-IN',
    hindi: 'hi-IN',
    tamil: 'ta-IN',
  };
  utterance.lang = langMap[language] || 'en-US';
  utterance.rate = 0.8;
  window.speechSynthesis.speak(utterance);
}

// тФАтФАтФА Helper: getAlphabetForLanguage (legacy) тФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФАтФА

export function getAlphabetForLanguage(language: string): LetterCard[] {
  switch (language) {
    case 'telugu': return teluguAlphabet;
    case 'hindi': return hindiAlphabet;
    case 'tamil': return tamilAlphabet;
    default: return englishAlphabet;
  }
}

